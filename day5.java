import java.io.*;
import java.util.*;
public class Main 
{
    public static void main(String[] args) 
    {
    Scanner sc = new Scanner(System.in);
    int N=sc.nextInt();
    for(int i=1;i<11;i++)
    {
          System.out.println(N +" x "+ i+" = "+(N*i));
    }	        
    }
}
    
